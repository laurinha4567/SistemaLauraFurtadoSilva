    private void jBtnAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnAlterarActionPerformed

        if (pesquisar == true) {
            incluir = false;
            habilitar(true);
            jTxtLfs_idVenda.setEnabled(false);
            jCboLfs_fk_vendedor.setEnabled(false);
            jCboLfs_fk_cliente.setEnabled(false);
        } else {
            JOptionPane.showMessageDialog(null, "Necessário pesquisar antes");
        }
    }//GEN-LAST:event_jBtnAlterarActionPerformed

    private void jBtnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnExcluirActionPerformed
        if (pesquisar == true) {
            int resp = JOptionPane.showConfirmDialog(null, "Deseja excluir ?");
            if (resp == JOptionPane.YES_OPTION) {

                Lfs_venda venda = viewBean();
                Lfs_vendaDao vendaDao = new Lfs_vendaDao();

                vendaDao.delete(venda);

            }
            limpar();
            habilitar(false);
        } else {
            JOptionPane.showMessageDialog(null, "Necessário pesquisar antes");
        }
    }//GEN-LAST:event_jBtnExcluirActionPerformed

    private void jBtnConfirmarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnConfirmarActionPerformed
        Lfs_venda venda = viewBean();
        Lfs_vendaDao vendaDao = new Lfs_vendaDao();
        if (incluir == true) {
            vendaDao.insert(venda);
        } else {
            vendaDao.update(venda);
        }

        pesquisar = false;
        incluir = false;

        habilitar(false);
        limpar();
    }//GEN-LAST:event_jBtnConfirmarActionPerformed

    private void jBtnCancelarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnCancelarActionPerformed
        incluir = false;
        pesquisar = false;
        habilitar(false);
        limpar();
    }//GEN-LAST:event_jBtnCancelarActionPerformed

