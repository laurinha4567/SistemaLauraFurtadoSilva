    public Lfs_venda viewBean() {
        Lfs_venda venda = new Lfs_venda();
        int cod = Integer.parseInt(jTxtLfs_idVenda.getText());
        venda.setLfs_idVenda(cod);

        try {
            SimpleDateFormat formato = new SimpleDateFormat("dd/MM/yyyy");
            venda.setLfs_dataVenda(formato.parse(jFmtLfs_dataVenda.getText()));
        } catch (ParseException ex) {
            System.getLogger(JDlgVenda.class.getName()).log(System.Logger.Level.ERROR, (String) null, ex);
        }

        venda.setLfs_fk_vendedor(jCboLfs_fk_vendedor.getSelectedIndex());
        venda.setLfs_fk_cliente(jCboLfs_fk_cliente.getSelectedIndex());
        venda.setLfs_observacoes(jTxtLfs_observacoes.getText());
        venda.setLfs_status_Venda(String.valueOf(jCboLfs_status_Venda.getSelectedIndex()));
        venda.setLfs_total(Double.parseDouble(jTxtLfs_total.getText()));

        return venda;
    }

    public void beanView(Lfs_venda venda) {
        jTxtLfs_idVenda.setText(String.valueOf(venda.getLfs_idVenda()));

        SimpleDateFormat formato = new SimpleDateFormat("dd/MM/yyyy");
        String dataVenda = formato.format(venda.getLfs_dataVenda());
        jFmtLfs_dataVenda.setText(dataVenda);

        jCboLfs_fk_vendedor.setSelectedIndex(-1);
        jCboLfs_fk_cliente.setSelectedIndex(-1);
        jTxtLfs_observacoes.setText(venda.getLfs_observacoes());
        jCboLfs_status_Venda.setSelectedIndex(Integer.parseInt(venda.getLfs_status_Venda()));
        jTxtLfs_total.setText(String.valueOf(venda.getLfs_total()));

    }

